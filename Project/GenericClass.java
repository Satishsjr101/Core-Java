import java.io.Console;

public class GenericClass {
    public static void main(String[] args) {
        String str = "one";

        switch (str) {

            case "one":
                System.out.println("1");
                break;

            case "two":
                System.out.println("@");
                break;

            default:
                System.out.println();
        }

    }

}
